def run() -> str:
    return """SCENE DOCUMENTATION GUIDE

1. Photograph (before anything is cleaned up):
   - The exact spot where you fell/were injured (wet floor, uneven surface, debris)
   - Any warning signs — or lack of signs — nearby
   - Your visible injury (if comfortable)
   - Wide-angle shot showing the full area context
   - Store/restaurant name, address, and any employee badges if visible

2. What NOT to say to the manager or other party:
   - Do NOT say "I'm fine" or "It was my fault" or "I wasn't paying attention"
   - Do NOT apologize ("I'm sorry" can be used against you)
   - Do NOT speculate about how the injury happened
   - DO say: "I need to document this. I'm going to take photos."

3. Incident report — fill out safely:
   - Write only facts: date, time, location, what you observed (wet floor, no sign)
   - Leave "cause of fall" blank or write "under investigation"
   - Do NOT admit fault or minimize injury
   - Request a copy before leaving — they may refuse; note that in your records
   - Do NOT sign a release of liability or medical authorization

4. Witness information:
   - Ask bystanders: "Did you see what happened? Can I have your name and number?"
   - Note employee names and what they said
   - If someone offers to help, get their contact info

5. Police:
   - For slip-and-fall at a business: police usually not required, but call if assault or serious injury
   - If police come: stick to facts only — "I slipped on a wet floor near aisle 4"
   - Do NOT give a recorded statement to store security or insurance at the scene"""
